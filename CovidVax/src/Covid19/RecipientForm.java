    private void jRTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jRTableMouseClicked
       DefaultTableModel model = (DefaultTableModel) jRTable.getModel();
       int selectedRowIndex = jRTable.getSelectedRow();
       jRecepientID.setText(model.getValueAt(selectedRowIndex,0).toString());
       jFullName.setText(model.getValueAt(selectedRowIndex,1).toString());
       jDateBirth.setText(model.getValueAt(selectedRowIndex,2).toString());
       jAddress.setText(model.getValueAt(selectedRowIndex,4).toString());
     sex=model.getValueAt(selectedRowIndex,3).toString();
       if (sex.equals("Male"))
           jMale.setSelected(true);
        else 
            if (sex.equals("Female"))
           jFemale.setSelected(true);
       
       Q1=model.getValueAt(selectedRowIndex,5).toString();
        if (Q1.equals("Yes"))
           jQY1.setSelected(true);
        else 
            if (Q1.equals("No"))
           jQN1.setSelected(true);
       Q2=model.getValueAt(selectedRowIndex,6).toString();
        if (Q2.equals("Yes"))
           jQY2.setSelected(true);
        else 
            if (Q2.equals("No"))
           jQN2.setSelected(true);
        Q3=model.getValueAt(selectedRowIndex,7).toString();
        if (Q3.equals("Yes"))
           jQY3.setSelected(true);
        else 
            if (Q3.equals("No"))
           jQN3.setSelected(true);
        Q4=model.getValueAt(selectedRowIndex,8).toString();
        if (Q4.equals("Yes"))
           jQY4.setSelected(true);
        else 
            if (Q4.equals("No"))
           jQN4.setSelected(true);
        Q5=model.getValueAt(selectedRowIndex,9).toString();
        if (Q5.equals("Yes"))
           jQY5.setSelected(true);
        else 
            if (Q5.equals("No"))
           jQN5.setSelected(true);
        Q6=model.getValueAt(selectedRowIndex,10).toString();
        if (Q6.equals("Yes"))
           jQY6.setSelected(true);
        else 
            if (Q6.equals("No"))
           jQN6.setSelected(true);
       preCDose=model.getValueAt(selectedRowIndex,11).toString();
       numDose=model.getValueAt(selectedRowIndex,12).toString();
        if (preCDose.equals("Pfizer-BioNTech") && numDose.equals("1")){
 
        jRC1.setSelected(true);
        }
        else 
            if (preCDose.equals("Pfizer-BioNTech") && numDose.equals("2")){
 
        jRC2.setSelected(true);
        }
        else 
           if (preCDose.equals("Moderna") && numDose.equals("1")){
 
        jRC3.setSelected(true);
        }
        else 
            if (preCDose.equals("Moderna") && numDose.equals("2")){
 
        jRC4.setSelected(true);
        }
        else 
            if (preCDose.equals("Johnson & Johnson's Janssen") && numDose.equals("1")){
    jRC5.setSelected(true);
    }//GEN-LAST:event_jRTableMouseClicked
    else 
            if (preCDose.equals("N/A") && numDose.equals("N/A")){
    jRC6.setSelected(true);
    }    
    }
