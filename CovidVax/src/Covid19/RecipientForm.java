    private void jAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAddActionPerformed
        addChange();
        if (jFullName.getText().equals("")||jDateBirth.getText().equals("")||sex.equals("")|| jAddress.getText().equals("")||Q1.equals("")||Q2.equals("")||Q3.equals("")||Q4.equals("")||Q5.equals("")||Q6.equals(""))
        infoBox("Please filling out your information", "Missing data");
        else
        {
           
            DefaultTableModel model = (DefaultTableModel)jRTable.getModel();
            if (model.getRowCount()>0){
           int  tempNum = Integer.parseInt(model.getValueAt(model.getRowCount()-1, 0).toString().substring(1));
          
        adminList.recipientIdNum=tempNum+1;
           }
           else 
               adminList.recipientIdNum++;
        adminList.RList.addRecipient("R"+String.valueOf(adminList.recipientIdNum), jFullName.getText(), jDateBirth.getText(),sex, jAddress.getText(),Q1,Q2,Q3,Q4,Q5,Q6,preCDose,numDose);
        adminList.RList.display();
        clearTable();
        }
        
    }//GEN-LAST:event_jAddActionPerformed
