    private void jAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAddActionPerformed
        
        if (jCName.getText().equals("")|| jVC1.getText().equals("")||jVC2.getText().equals("")||jVC3.getText().equals(""))
        infoBox("Please filling out your information", "Missing data");
        else
        {
            totalDose =   Integer.parseInt(jVC1.getText())+Integer.parseInt(jVC2.getText())+Integer.parseInt(jVC3.getText());
 jTotal.setText(String.valueOf(totalDose));
        DefaultTableModel model = (DefaultTableModel)jCTable.getModel();
        if (model.getRowCount()>0){   
        int  tempNum = Integer.parseInt(model.getValueAt(model.getRowCount()-1, 0).toString().substring(1));
          
        adminList.clinicianIdNum=tempNum+1;
        }
        else
        adminList.clinicianIdNum++;
        adminList.CList.addClinician("C"+String.valueOf(adminList.clinicianIdNum), jCName.getText(), jVC1.getText(),jVC2.getText(),jVC3.getText(),jTotal.getText());
        adminList.CList.display();
        clearTable2();
        }
       
    }                                    
     
